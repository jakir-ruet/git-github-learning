# git branch -M master > making default branch name
# git remote add origin https://github.com/userName/repositoryName.git > syntax
# git remote add origin https://github.com/jakir-ruet/git-github-learning.git
# git push -u origin master > push the project to live github.com
# -u (upstream) flag creates a tracking reference for every branch that you successfully push onto the remote repository.